#imprimir idicando el indice indice
listas = [5,9,"luis",-6,"estudiante"]
print (listas[0])
print (listas[-1])
#borrar indicando el indice de la lista
del listas[2]
print(listas)
#Remover un valor de la lista
numeros = [1,2,3,4,9,6,7,8,9]
numeros.remove(2)
print(numeros)
#consultar dando el valor y te da el indice
numeros = [1,2,3,4,9,6,7,8,9]
print(numeros.index(3))
#imprimir desde indices especificos
numeros = [1,2,3,4,9,6,7,8,9]
print (numeros)
print (numeros[0:])
#Sustituir un valor de la lista
